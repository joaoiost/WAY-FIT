// Normalized for fast search
const NORMALIZED = EXERCISE_LIBRARY.map(e => ({
  ...e,
  _search: e.name.toLowerCase()
    .normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9 ]/g, ''),
}));

export function searchExercises(query, limit = 8) {
  if (!query || query.length < 2) return [];
  const q = query.toLowerCase()
    .normalize('NFD').replace(/[̀-ͯ]/g, '')
    .replace(/[^a-z0-9 ]/g, '');
  return NORMALIZED
    .filter(e => e._search.includes(q))
    .slice(0, limit);
}
